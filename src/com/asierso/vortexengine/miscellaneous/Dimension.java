package com.asierso.vortexengine.miscellaneous;

/**
 * Represents dimension in with and height as a Vector2
 * @author Asierso
 */
public final class Dimension {

    public int width;
    public int height;

    public Dimension(int width, int height) {
        this.width = width;
        this.height = height;
    }

}
