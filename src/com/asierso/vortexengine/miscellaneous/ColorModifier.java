package com.asierso.vortexengine.miscellaneous;

/**
 * Color that can be modified in execution time. Using modifier controllers
 * @author Asierso
 */
public class ColorModifier {

    public int r;
    public int g;
    public int b;
    public int a;
    public ColorModifier(int r,int g, int b, int a){
        this.r = r;
        this.g = g;
        this.b = b;
        this.a = a;
    }
}
