package com.asierso.vortexengine.miscellaneous;

/**
 *
 * @author asierso
 */
public class EntityFlag<T> {
    private String key;
    private T value;
    public EntityFlag(String key, T value){
        this.key = key;
        this.value = value;
    }

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }

    public T getValue() {
        return value;
    }

    public void setValue(T value) {
        this.value = value;
    }    
}
